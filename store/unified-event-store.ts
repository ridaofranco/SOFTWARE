import { create } from "zustand"
import { persist } from "zustand/middleware"

// Types
export interface Event {
  id: string
  title: string
  date: string
  venue: string
  status: "pending" | "confirmed" | "cancelled"
  type: "propio" | "privado" | "feriado"
  description?: string
  theme?: string
  emoji?: string
  budget?: number
  attendees?: number
  address?: string
  openingTime?: string
  closingTime?: string
  setupStartTime?: string
  teardownTime?: string
  loadingPoint?: string
  producerContact?: {
    name: string
    phone: string
    email: string
  }
  hasLocalPermit?: boolean
  hasAlcoholPermit?: boolean
  notes?: string
  providers?: any[]
  venueId?: string
}

export interface Venue {
  id: string
  nombre: string
  location: string
  capacity: number
  type: "club" | "salon" | "outdoor" | "teatro"
  contact: string
  rating: number
}

export interface Provider {
  id: string
  name: string
  category: string
  contact: string
  rating?: number
  services: string[]
  priceRange: string
  item?: string
  price?: string
  paymentMethod?: string
  status?: string
  staff?: string
  insurance?: string
  deadline?: string
  observations?: string
  blocked?: boolean
  blockReason?: string
}

export interface Task {
  id: string
  title: string
  description: string
  status: "pending" | "in-progress" | "completed"
  priority: "low" | "medium" | "high"
  assignee: string // Cambiar de assignedTo a assignee para consistencia
  dueDate: string
  eventId?: string
  category: string
  isAutomated?: boolean
  automatedTaskType?: "pasajes" | "hoteleria" | "aduana_ida" | "aduana_vuelta"
  daysUntilEvent?: number
  createdAt?: string
  updatedAt?: string
}

export interface Contract {
  id: string
  type: "bailarin" | "tecnico"
  name: string
  email: string
  phone: string
  rate: number
  status: "active" | "inactive" | "draft" | "sent" | "signed"
  startDate: string
  endDate?: string
  skills: string[]
  eventIds: string[]
  providerName?: string
  contractorName?: string
  amount?: number
  date?: string
}

export interface BudgetItem {
  id: string
  area: string
  item: string
  cantidad: number
  precioUnitario: number
  precioTotal: number
  resultadoNegociacion: number
  desviacion?: number
  status: "pending" | "approved" | "paid"
  vendor?: string
  eventId: string
  category?: string
  description?: string
  estimatedCost?: number
  actualCost?: number
}

export interface ChatMessage {
  id: string
  content: string
  message: string
  sender: string
  timestamp: string
  type: "text" | "file" | "image"
  eventId?: string
}

export interface Chat {
  id: string
  name: string
  participants: string[]
  messages: ChatMessage[]
  lastMessage?: string
  lastActivity: string
  eventId?: string
}

interface UnifiedEventStore {
  // State
  events: Event[]
  venues: Venue[]
  providers: Provider[]
  tasks: Task[]
  contracts: Contract[]
  budgetItems: BudgetItem[]
  chats: Chat[]
  chatMessages: ChatMessage[]

  // Event actions
  addEvent: (event: Event) => void
  updateEvent: (id: string, event: Partial<Event>) => void
  deleteEvent: (id: string) => void
  getEvent: (id: string) => Event | undefined

  // Venue actions
  addVenue: (venue: Venue) => void
  updateVenue: (id: string, venue: Partial<Venue>) => void
  deleteVenue: (id: string) => void
  getVenue: (id: string) => Venue | undefined

  // Provider actions
  addProvider: (provider: Partial<Provider>) => void
  updateProvider: (id: string, provider: Partial<Provider>) => void
  deleteProvider: (id: string) => void
  getProvider: (id: string) => Provider | undefined
  rateProvider: (id: string, rating: number) => void
  blockProvider: (id: string, reason: string) => void

  // Task actions
  addTask: (task: Task) => void
  updateTask: (id: string, task: Partial<Task>) => void
  deleteTask: (id: string) => void
  getTask: (id: string) => Task | undefined

  // Contract actions
  addContract: (contract: Contract) => void
  updateContract: (id: string, contract: Partial<Contract>) => void
  deleteContract: (id: string) => void
  getContract: (id: string) => Contract | undefined

  // Budget actions
  addBudgetItem: (item: BudgetItem) => void
  updateBudgetItem: (id: string, item: Partial<BudgetItem>) => void
  deleteBudgetItem: (id: string) => void
  getBudgetItem: (id: string) => BudgetItem | undefined

  // Chat actions
  addChat: (chat: Chat) => void
  updateChat: (id: string, chat: Partial<Chat>) => void
  deleteChat: (id: string) => void
  getChat: (id: string) => Chat | undefined
  addMessage: (chatId: string, message: ChatMessage) => void
}

